package me.june.mvc.propertyeditor;

public class Member {

    int id;
    int age;
    Code userType;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public Code getUserType() {
        return userType;
    }

    public void setUserType(Code userType) {
        this.userType = userType;
    }
}
